// este js se inicia con caps porque tiene clase y por convencion los archivos con clase se inician con mayuscula
// la clase se declara con class y el nombre  + llaves correspondientes
class calculadora {
    sumar(num1, num2) {
        return num1 + num2;
    }

    restar(num1, num2) {
        return num1 - num2;
    }

    dividir(num1, num2) {
        return num1 / num2;
    }

    multiplicar(num1, num2) {
        return num1 * num2;
    }
}